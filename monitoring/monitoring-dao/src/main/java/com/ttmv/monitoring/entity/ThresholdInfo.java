package com.ttmv.monitoring.entity;

import java.math.BigInteger;

public class ThresholdInfo {
    
	private BigInteger id;							//ID
    private String thresholdName;			//阀值名称
    private String thresholdValue;				//阀值的值
    private String thresholdType;				//阀值类型
    private String thresholdAlerterIds;		//阀值报警ID
    private BigInteger thresholdSet;			//阀值设置
    private String thresholdShowName;   //阀值中文名称
    
    public BigInteger getId() {
        return id;
    }

    public void setId(BigInteger id) {
        this.id = id;
    }

    public String getThresholdName() {
        return thresholdName;
    }

    public void setThresholdName(String thresholdName) {
        this.thresholdName = thresholdName;
    }

    public String getThresholdValue() {
        return thresholdValue;
    }

    public void setThresholdValue(String thresholdValue) {
        this.thresholdValue = thresholdValue;
    }

    public String getThresholdType() {
        return thresholdType;
    }

    public void setThresholdType(String thresholdType) {
        this.thresholdType = thresholdType;
    }

    public String getThresholdAlerterIds() {
        return thresholdAlerterIds;
    }

    public void setThresholdAlerterId(String thresholdAlerterIds) {
        this.thresholdAlerterIds = thresholdAlerterIds;
    }

    public BigInteger getThresholdSet() {
        return thresholdSet;
    }

    public void setThresholdSet(BigInteger thresholdSet) {
        this.thresholdSet = thresholdSet;
    }

	public String getThresholdShowName() {
		return thresholdShowName;
	}

	public void setThresholdShowName(String thresholdShowName) {
		this.thresholdShowName = thresholdShowName;
	}

	public void setThresholdAlerterIds(String thresholdAlerterIds) {
		this.thresholdAlerterIds = thresholdAlerterIds;
	}
}