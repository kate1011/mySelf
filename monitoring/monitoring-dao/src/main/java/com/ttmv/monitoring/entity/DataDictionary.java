package com.ttmv.monitoring.entity;

import java.math.BigInteger;

public class DataDictionary {
	
    private BigInteger id;					//ID
    private String dataKey;				//数据字典KEY
    private String dataValue;				//数据字典Value
    private String type;						//数据字典类型
    private String extendField2;			//扩展字段2
    private String extendField3;			//扩展字段3

    public BigInteger getId() {
        return id;
    }

    public void setId(BigInteger id) {
        this.id = id;
    }

    public String getDataKey() {
        return dataKey;
    }

    public void setDataKey(String dataKey) {
        this.dataKey = dataKey;
    }

    public String getDataValue() {
        return dataValue;
    }

    public void setDataValue(String dataValue) {
        this.dataValue = dataValue;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getExtendField2() {
        return extendField2;
    }

    public void setExtendField2(String extendField2) {
        this.extendField2 = extendField2;
    }

    public String getExtendField3() {
        return extendField3;
    }

    public void setExtendField3(String extendField3) {
        this.extendField3 = extendField3;
    }
}