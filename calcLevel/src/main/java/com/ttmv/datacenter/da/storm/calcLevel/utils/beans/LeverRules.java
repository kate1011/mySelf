public class LeverRules {

    //保级需要的经验
    private Long lowestexp;
    //降级的扣减值
    private Long demotionRate;
    //等级
    private Long grade;
    //类型
    private String gradeType;
    //说明
    private String gradeMsg;
    //升级经验
    private Long upgradeExp;
    //总经验
    private Long sumExp;

    public Long getLowestexp() {
        return lowestexp;
    }

    public void setLowestexp(Long lowestexp) {
        this.lowestexp = lowestexp;
    }

    public Long getDemotionRate() {
        return demotionRate;
    }

    public void setDemotionRate(Long demotionRate) {
        this.demotionRate = demotionRate;
    }

    public Long getGrade() {
        return grade;
    }

    public void setGrade(Long grade) {
        this.grade = grade;
    }

    public String getGradeType() {
        return gradeType;
    }

    public void setGradeType(String gradeType) {
        this.gradeType = gradeType;
    }

    public String getGradeMsg() {
        return gradeMsg;
    }

    public void setGradeMsg(String gradeMsg) {
        this.gradeMsg = gradeMsg;
    }

    public Long getUpgradeExp() {
        return upgradeExp;
    }

    public void setUpgradeExp(Long upgradeExp) {
        this.upgradeExp = upgradeExp;
    }

    public Long getSumExp() {
        return sumExp;
    }

    public void setSumExp(Long sumExp) {
        this.sumExp = sumExp;
    }

    @Override
    public String toString() {
        return "LeverRules{" +
                "lowestexp=" + lowestexp +
                ", demotionRate=" + demotionRate +
                ", grade=" + grade +
                ", gradeType='" + gradeType + '\'' +
                ", gradeMsg='" + gradeMsg + '\'' +
                ", upgradeExp=" + upgradeExp +
                ", sumExp=" + sumExp +
                '}';
    }
}
